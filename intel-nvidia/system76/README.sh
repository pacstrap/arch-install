#!/bin/bash

################################################################################
# enable system76-power service
################################################################################

sudo systemctl enable --now system76-power.service

################################################################################
# check status of system76-power.service
################################################################################

systemctl status system76-power.service

################################################################################
# system76-power help
################################################################################

system76-power --help

################################################################################
# change graphics card integrated/hybrid/nvidia
################################################################################

sudo system76-power graphics integrated

reboot

lspci # check graphics card...
